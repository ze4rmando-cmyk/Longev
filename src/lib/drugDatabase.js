export function searchDrugs(query) {
  if (!query || query.trim().length < 2) return DRUG_DATABASE;
  const q = query.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  return DRUG_DATABASE.filter(d => {
    const name = d.name.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    const cls = d.class.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    const ind = d.indication.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    const grp = d.group.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    return name.includes(q) || cls.includes(q) || ind.includes(q) || grp.includes(q) || d.tags.some(t => t.normalize("NFD").replace(/[\u0300-\u036f]/g, "").includes(q));
  });
}

export function getDrugById(id) {
  return DRUG_DATABASE.find(d => d.id === id) || null;
}

export function getDrugsByTag(tag) {
  return DRUG_DATABASE.filter(d => d.tags.includes(tag));
}